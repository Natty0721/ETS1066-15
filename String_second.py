# lower() String
text0 = "NATNAEL AYALEW"
print(text0.lower())  # Output: natnael ayalew

text1 = "PYTHON PROGRAMMING"
print(text1.lower())  # Output: python programming

text2 = "Hello ETHIOPIA 1234"
print(text2.lower())  # Output: hello ethiopia 1234

text3 = "MeChaTronics MiXeD CaSe"
print(text3.lower())  # Output: mechatronics mixed case

# upper() String
text0 = "natnael ayalew"
print(text0.upper())  # Output: NATNAEL AYALEW

text1 = "python programming"
print(text1.upper())  # Output: PYTHON PROGRAMMING

text2 = "hello ethiopia 1234"
print(text2.upper())  # Output: HELLO ETHIOPIA 1234

text3 = "MeChaTronics MiXeD CaSe"
print(text3.upper())  # Output: MECHATRONICS MIXED CASE

# title() String
text0 = "natnael ayalew"
