# capitalize() String
text1 = "natnael ayalew"
print(text1.capitalize())  # Output: Natnael ayalew

text2 = "pYTHON proGRAMming"
print(text2.capitalize())  # Output: Python programming

text3 = "python 1234"
print(text3.capitalize())  # Output: Python 1234

# swapcase() String
text1 = "Natnael Ayalew"
print(text1.swapcase())  # Output: nATNAEL aYALEW

text2 = "PyThOn ProGraMMing"
print(text2.swapcase())  # Output: pYtHoN pROgRAmMING

text3 = "Python 1234"
print(text3.swapcase())  # Output: pYTHON 1234

# find() String
text1 = "Natnael Ayalew is a student"
print(text1.find("Ayalew"))  # Output: 8 (Index where "Ayalew" starts)

text2 = "Python programming is fun"
print(text2.find("gram"))  # Output: 10

text3 = "Python 1234"
print(text3.find("robot"))  # Output: -1 (Substring not found)
