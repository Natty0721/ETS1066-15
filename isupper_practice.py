# Practice 1

text1 = "HELLO"
text2 = "Hello"
text3 = "123!"
text4 = "PYTHON123"

print(text1.isupper())  # True (All uppercase)
print(text2.isupper())  # False (Contains lowercase letters)
print(text3.isupper())  # False (No uppercase letters)
print(text4.isupper())  # True (Numbers don't affect the result)

